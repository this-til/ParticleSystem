package com.til.particle_system.util;

/***
 * @author til
 */
public class V3 {
    public final double x;
    public final double y;
    public final double z;

    public V3() {
        this.x = 0;
        this.y = 0;
        this.z = 0;
    }

    public V3(double x, double y, double z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
